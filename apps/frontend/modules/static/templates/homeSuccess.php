    <h1>Introduction</h1>
    <p>
        Please take a moment to read through this text to get
        a basic understanding of the current status and available
        functionalities of the current version of the Regional
        Integration Knowledge System.
    </p>

    <p>
        Regional integration agreements have increased in number, scope and diversity
        over the last two decades of the twentieth century. In the twenty-first century,
        the trend towards regional cooperation looks set to continue as governments and
        states opt for cooperation to serve a variety of purposes and to secure common interests.
        Regional cooperation serves to reconcile the tensions between globalisation pressures and
        the demands for greater local autonomy, while offering states an opportunity to retain
        their own autonomy over political processes. However, in spite of the continuing
        development of this (supra-national) regional governance level, the provision of systematic,
        detailed and comparable information on regional integration processes worldwide
        seems still to lag behind.
    </p>

    <p>
        The Regional Integration Knowledge System is a joint initiative
        taken by UNU-CRIS and the GARNET Network of Excellence. It has the
        ambition to provide a central node for information exchange on regional
        integration processes worldwide and to constitute a ‘missing link’ between
        the existing regional initiatives for information provision on regional
        integration and cooperation.
    </p>

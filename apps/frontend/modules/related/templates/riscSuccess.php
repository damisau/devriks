<H1>Consortium for Comparative Research on Regional Integration and Social
    Cohesion (RISC)</H1>
<I><p>
        The mission of the Consortium for Comparative Research on
        Regional Integration and Social Cohesion (RISC) is to promote
        the comparative examination of the human impact of various
        aspects of regional integration across geographic areas and
        time periods through the creation of a cross-regional and
        interdisciplinary network.
    </p>
    <p>
        Moreover, the research conducted through this consortium
        could eventually support social action projects in local
        communities through improved understanding of evolving
        political and economic contexts.
    </p>

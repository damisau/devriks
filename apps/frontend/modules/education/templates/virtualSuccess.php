<H1>UNCTAD virtual institute on trade and development</h1>
<br>UNCTAD about it's virtual institute:
<p>
<I>
<img src="../images/largevilogo.gif" style="float:right">
In today's global economy, better skilled and informed personnel working
in the field of trade is a prerequisite for the economic and social development
of developing countries. The rationale of the UNCTAD Virtual Institute (Vi) is
to strengthen the capacities of developing country knowledge institutions to
provide high quality teaching, training and research on trade and development
issues.
</p>
<p>
Knowledge institutions, such as universities and research networks, are a
vital source of information and training in developing countries. They provide
skills and understanding for entrepreneurs and producers, for officials and
civil servants and for policy makers and negotiators. In addition, they also
offer substantive empirical research required for informed policymaking and
negotiation.
</p>
<p>
The Virtual Institute is in fact a network of members, comprising a small
secretariat based in Geneva, 55 member universities across five continents that
own and determine the work plan of the Virtual Institute.
</p>
<p>
The Vi's first goal is to provide high quality support to its university members.
For this reason, the program concentrates on a few members, expanding gradually,
not compromising the level of excellence it can offer to its membership.
However, in order to reach a wider audience, the Vi offers an affiliate
membership to universities in member countries and an "associate membership"
to more than 1,639 individuals in 148 countries who benefit from selected services
offered by the Vi.
</i><br><br>
